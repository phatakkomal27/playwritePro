// Truthy and Falsy values in JavaScript
if(" ") console.log("This is a string with a space and it is truthy");
if(1) console.log("This is one and it is truthy");
if(-1) console.log("This is negative one and it is truthy");

if(0) console.log("This is zero and it is falsy");
if(false) console.log("This is false"); 
if("") console.log("This is an empty string and it is falsy");
if(null) console.log("This is null");
if(undefined) console.log("This is undefined");
if(NaN) console.log("This is NaN"); 